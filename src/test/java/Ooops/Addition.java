package Ooops;

public class Addition {

    public void addValues(int a, int b){
        int c =a +b;
        System.out.println(c);
    }

    public void addValues(int a, int b, int c){
        int d = a +b+c;
        System.out.println(d);
    }

    public double addValues(double a, double b){
        double c = a +b;
        return c;
    }
}
